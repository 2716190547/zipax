export function messages(locale: Locale) {
  return { ...en, ...aliases[locale] };
}

export function matchLocale(value: string): Locale {
  const normalized = value.toLowerCase();
  return locales.find((locale) => normalized === locale.key.toLowerCase())?.key
    ?? locales.find((locale) => normalized.startsWith(locale.key.split("-")[0].toLowerCase()))?.key
    ?? "en-US";
}
