export function resolveLocale(mode: LanguageMode, browserLanguage = navigator.language): Locale {
  if (mode !== "system") return mode;
  const lower = browserLanguage.toLowerCase();
  if (lower.startsWith("zh-tw") || lower.startsWith("zh-hk") || lower.startsWith("zh-mo") || lower.includes("hant")) return "zh-TW";
  if (lower.startsWith("zh")) return "zh-CN";
  if (lower.startsWith("es")) return "es-ES";
  if (lower.startsWith("ar")) return "ar";
  if (lower.startsWith("id")) return "id-ID";
  if (lower.startsWith("pt")) return "pt-BR";
  if (lower.startsWith("fr")) return "fr-FR";
  if (lower.startsWith("ja")) return "ja-JP";
  if (lower.startsWith("ko")) return "ko-KR";
  return "en-US";
}

export function isRtl(locale: Locale) {
  return locale === "ar";
}

export function useI18n() {
  const languageMode = useAppStore((s) => s.languageMode);
  const locale = resolveLocale(languageMode);
  return useMemo(() => {
    const dict = messages[locale] || messages["en-US"];
    const t = (key: string, vars?: Record<string, string | number>) => {
      const template = dict[key] || messages["en-US"][key] || key;
      return Object.entries(vars || {}).reduce(
        (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
        template,
      );
    };
    return { locale, t };
  }, [locale]);
}
